
package ejercitacion.pkgdo.pkgwhile;

/**
 *
 * @author LECCESE
 */
public class EjercitacionDoWhile {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        
        int contador=0;
        
        do{
            System.out.println("Estoy en la vuelta "+contador);
            contador ++;
        }while(contador<10);
    
    }    
}
